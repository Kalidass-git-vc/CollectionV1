package Collection;

public class Array_ {

	public static void main(String[] args) {

		//Array
		int Arr[]= {1,2,3,4,5,5,6,6,7,8,9};
		
		for(int x:Arr) {
			System.out.println(x);			
		}
		
		//Object type to store any data type
		
		Object obj[]= {1,3,4,"kalidass",1.2,4,'A'};
		
		for(Object x:obj) {
			System.out.println(x);
		}
		
		
	}

}
